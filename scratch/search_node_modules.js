const fs = require('fs');
const path = require('path');

const SEARCH_TERMS = ['cl_keys', 'auth_keys'];

function walkDir(dir, callback) {
  const files = fs.readdirSync(dir);
  for (const file of files) {
    const filePath = path.join(dir, file);
    const stat = fs.statSync(filePath);
    if (stat.isDirectory()) {
      if (file !== '.git') {
        walkDir(filePath, callback);
      }
    } else {
      callback(filePath);
    }
  }
}

console.log('Searching node_modules for key terms...');

walkDir(path.join(__dirname, '..', 'node_modules'), (filePath) => {
  try {
    const content = fs.readFileSync(filePath, 'utf8');
    const lines = content.split(/\r?\n/);
    
    lines.forEach((line, index) => {
      SEARCH_TERMS.forEach((term) => {
        if (line.includes(term)) {
          console.log(`Found "${term}" in ${path.relative(path.join(__dirname, '..'), filePath)} at line ${index + 1}:`);
          console.log(`  > ${line.trim()}`);
        }
      });
    });
  } catch (e) {
    // Ignore binary files or read errors
  }
});

console.log('Search finished.');
